<script module>
    import { cva } from "class-variance-authority";

    const variants = cva(
        "group/alert relative grid w-full gap-0.5 rounded-lg border px-2.5 py-2 text-left text-sm has-data-[slot=alert-action]:relative has-data-[slot=alert-action]:pr-18 has-[>svg]:grid-cols-[auto_1fr] has-[>svg]:gap-x-2 *:[svg]:row-span-2 *:[svg]:translate-y-0.5 *:[svg]:text-current *:[svg:not([class*='size-'])]:size-4",
        {
            variants: {
                variant: {
                    default: "bg-card text-card-foreground",
                    destructive:
                        "bg-card text-destructive *:data-[slot=alert-description]:text-destructive/90 *:[svg]:text-current",
                },
            },
            defaultVariants: {
                variant: "default",
            },
        },
    );

    /** @typedef {import('class-variance-authority').VariantProps<typeof variants>} AlertVariants */
</script>

<script>
    /** @type {import('svelte/elements').HTMLAttributes<HTMLDivElement> & AlertVariants} */
    const { children, ...props } = $props();
</script>

<div role="alert" {...props} class={[variants({ class: props.class, variant: props.variant })]}>
    {@render children?.()}
</div>
